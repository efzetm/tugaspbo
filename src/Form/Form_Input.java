    private boolean validasi() {
        boolean Terisi = true;
        String pesanError = "DATA TIDAK BOLEH KOSONG";

        if (TxtNama.getText().equals("")
                || TxtTtl.getText().equals("")
                || TxtNim.getText().equals("")
                || TxtNama.getText().equals("")
                || TxtAlamat.getText().equals("")) {
            Terisi = false;
        }

        if (!Terisi) {
            JOptionPane.showMessageDialog(this, pesanError, "Error ", JOptionPane.ERROR_MESSAGE);
        }

        return Terisi;
    }

