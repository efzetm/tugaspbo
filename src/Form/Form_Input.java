    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        if (validasi()) {
            String sql = "insert into data_mhs values (?,?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, TxtNama.getText());
                stat.setString(2, TxtNim.getText());
                stat.setString(3, TxtTtl.getText());
                stat.setString(4, kel);
                stat.setString(5, CbAgama.getSelectedItem().toString());
                stat.setString(6, TxtAlamat.getText());

                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "DATA BERHASIL DISIMPAN");
                kosong();

                this.setVisible(false);
                new Form_Data().setVisible(true);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "DATA GAGAL DISIMPAN" + e);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_BtnSimpanActionPerformed

    public void Agama() {
        if (CbAgama.getItemCount() > 1) {
            for (int i = 1; i < CbAgama.getItemCount(); i++) {
                CbAgama.removeItemAt(i);
            }
        }
        for (int i = 0; i < Agama.length; i++) {
            CbAgama.addItem(Agama[i]);
        }
    }

